public class Math {
    public float twoTime(float num) {
        System.out.println("실수 2배 적용합니다.");
        return num * 2;
    }

    public float halfTime(float num) {
        System.out.println("실수 1/2배 적용합니다.");
        return num / 2;
    }
}
